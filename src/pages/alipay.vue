<template>
    <div class="ali-pay">
      <loading v-if="loading"></loading>
        <div class="form" v-html="content"></div>
    </div>
</template>

<script>
import Loading from './../components/Loading';
export default {
  name: 'alipay',
  components: {
    Loading
  },
  data () {
    return {
      orderId: this.$route.query.orderId,
      content: '',//返回为一个form表单
      loading: true
    }
  },
  mounted () {
    this.paySubmit();
  },
  methods: {
    paySubmit () {
      this.axios.post('/pay',{
        orderId: this.orderId,
        orderName: 'Vue高仿小米商城',
        amount: 0.01,
        payType: 1
      }).then((res) => {
        this.content = res.content;
        setTimeout(() => {
          //提交接口返回的表单后，跳转到支付宝支付页面
          document.forms[0].submit();
        },1000)
      })
    }
  }
}
</script>